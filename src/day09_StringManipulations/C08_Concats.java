package day09_StringManipulations;

public class C08_Concats {

    public static void main(String[] args) {
        String str1 = "Ali";

        String str2 = "Veli";
        String str3 ="John";
        int in1 = 3;
        int in2 = 4;

        System.out.println(str1.concat(" "+str2).concat(" "+str3).concat(""+in1+in2)); //Ali Veli John


    }

}
