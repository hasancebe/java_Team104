package day05_increments_ifElseStatements;

public class C01_Concatenations {

    public static void main(String[] args) {

        String s1 = "Java";
        String s2 = " ";
        String s3 = "easy";
        String s4 ="";

        int a =3;
        int b =4;



        //Java12easy
        System.out.println(s1+a*b+s3);


        //34Java easy
        System.out.println(s4+a+b+s1+s2+s3);


        //7 Java easy

        System.out.println(a+b+s2+s1+s2+s3);


        //12 Java easy

        System.out.println(a*b+s2+s1+s2+s3);






    }


}
