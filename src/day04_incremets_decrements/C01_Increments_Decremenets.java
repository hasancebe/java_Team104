package day04_incremets_decrements;

public class C01_Increments_Decremenets {

    public static void main(String[] args) {

        int number = 10;

        //let's increase the number by 3 and print it on the screen

        number = number +3 ;

        System.out.println(number); //13

        number += 3;

        System.out.println(number);  //16

        number++;

        System.out.println(number);


        int n = 10;

        n = n + 1 ; // 11

        n+=1 ;   //  12

        n++ ;  // 13

        ++n ;  //14

        System.out.println("n = "+ n);


        int t =10;

        t= t-1 ;  //9
        t -=1  ; // 8
        t--  ;  // 7
        --t  ;  // 6

        System.out.println("t = "+t);








    }




}
